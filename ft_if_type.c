#include "ft_printf.h"

int ft_if_type(char c)
{
	if (c == 's' || c == 'S' || c == 'p' || c == 'd' || c == 'D' || c == 'i' ||
			c == 'o' || c == 'O' || c == 'u' || c == 'U' || c  == 'x' || c == 'X'
			|| c == 'c' || c == 'C')
		return (1);
	return (0);
}

int ft_if_digital(char c)
{
	if (c == 'd' || c == 'i' || c == 'D' || c == 'u' || c == 'U' || c == 'o' || c == 'O' ||
			c == 'x' || c == 'X')
		return (1);
	return (0);
}
