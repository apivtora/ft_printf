#include "ft_printf.h"

int ft_size_u(wchar_t c)
{
	if (c < 128)
		return (1);
	else if (c > 127  && c < 0x0800)
		return (2);
	else if ((c > 0x07ff) && c < 0x10000)
		return (3);
	else if (c > 0xFFFF && c < 110000)
		return (4);
	return (0);
}
